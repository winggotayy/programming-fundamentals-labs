// 结构体表示题目
struct Question
{
	wchar_t content[1000];		// 题目内容
	wchar_t optionA[1000];		// 选项 A
	wchar_t optionB[1000];		// 选项 B
	wchar_t optionC[1000];		// 选项 C
	wchar_t answer[1000];		// 答案
};

vector<Question> questions;

Question new_Question(
	const wchar_t* content,
	const wchar_t* optionA,
	const wchar_t* optionB,
	const wchar_t* optionC,
	const wchar_t* answer
) {
	Question new_ques = {};
	wcscpy_s(new_ques.content, content);
	wcscpy_s(new_ques.optionA, optionA);
	wcscpy_s(new_ques.optionB, optionB);
	wcscpy_s(new_ques.optionC, optionC);
	wcscpy_s(new_ques.answer, answer);
	return new_ques;
}

